function checkProbabilityTheory(count) { /*оголошення функції, яка приймає один параметр count. 
    Цей параметр буде відповідати кількості випадкових чисел, які потрібно згенерувати.*/
    let evenCount = 0; // створення змінної для підрахунку парних чисел
    let oddCount = 0;  // створення змінної для підрахунку непарних чисел

    for (let i = 0; i < count; i++) { /*запускаємо цикл, який буде виконуватися count разів 
        (це те значення, яке передається при виклику функції) */
        // Генеруємо випадкове число в діапазоні від 100 до 1000 включно
        let randomNumber = Math.floor(Math.random() * (1000 - 100 + 1)) + 100; /* Math.random() генерує число від 0 до 1
         (не включаючи 1)
         множимо це число на (1000 - 100 + 1), щоб отримати число в діапазоні від 0 до 900
         Math.floor() округлює число до меншого цілого значення.
         Додаємо 100, щоб результат був у діапазоні від 100 до 1000.*/


        // Перевіряємо, чи число парне
        if (randomNumber % 2 === 0) {
            evenCount++; // Якщо парне, збільшуємо лічильник парних чисел
        } else {
            oddCount++; // Якщо непарне, збільшуємо лічильник непарних чисел
        }
    }

    // Обчислюємо відсотки
    let evenPercentage = (evenCount / count) * 100;
    let oddPercentage = (oddCount / count) * 100;
    /* обчислюємо відсотки для парних і непарних чисел:
       кількість парних чисел ділиться на загальну кількість чисел (count) і множиться на 100, щоб отримати відсоток.
       Те саме для непарних. */

    // Виводимо результати
    console.log(`Кількість згенерованих чисел: ${count}`);
    console.log(`Парних чисел: ${evenCount}`);
    console.log(`Не парних чисел: ${oddCount}`);
    console.log(`Відсоток парних: ${evenPercentage.toFixed(2)}%`);
    console.log(`Відсоток непарних: ${oddPercentage.toFixed(2)}%`); /* Який відсоток кожної групи. 
    Функція toFixed(2) використовується для відображення двох знаків після коми у відсотках */
}

// Приклад використання функції
checkProbabilityTheory(100);
